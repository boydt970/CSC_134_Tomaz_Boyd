//include the iostream directive
#include <iostream>

//use the standard namespace 
using namespace std;

//define the main() function/method
int main()
{
    double V;
    double pi;
    double radius;
    pi = 3.1415;//pi math measurement
    radius = 7 * 7 * 7;//radius 7 to the 3rd power
    V = 4.0/3.0 * (pi * radius);//equation to determine volume
    //printing volume of a sphere
    std::cout << "Volume equals " << V << std::endl;

    return 0;
}