//include the iostream directive
#include <iostream>

//use the standard namespace 
using namespace std;

//define the main() function/method
int main()
{
    double pi;
    double radius;
    double A;
    pi = 3.1415;//empty varible to be assigned later
    radius = 19 * 19;//fahrenheit degrees
    A = pi * radius;//equation to determine C/Celsius
    //printing temperatures in both forms
    std::cout << "Area equals " << A << std::endl;

    return 0;
}